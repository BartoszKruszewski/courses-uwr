// Inicjalizacja PWM na pinie PD6 (OC0A - Timer0)
void pwm_init(void) {
    // Ustaw PD6 jako wyjście
    DDRD |= (1 << DDD6);
    
    // Fast PWM, non-inverting mode
    TCCR0A = (1 << COM0A1) | (1 << WGM01) | (1 << WGM00);
    
    // Prescaler = 64 (PWM freq ≈ 976 Hz przy 16MHz)
    TCCR0B = (1 << CS01) | (1 << CS00);
    
    // Początkowa wartość duty cycle
    OCR0A = 0;
}

int main(void) {
    pwm_init();
    adc_init(ADC_REF_AVCC, ADC_PRESCALER_128);
    while (1) OCR0A = pgm_read_byte(&gamma_table[adc_read_average(ADC_CHANNEL_0, 16) >> 2]);
    return 0;
}
